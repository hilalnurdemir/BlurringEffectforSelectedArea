% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)

global image

[file_name path_name]= uigetfile();
if file_name==0
    msgbox(sprintf('Please Select an Image'),'error','error')%when there is no image program gives this error
end

image = imread(file_name);
axes(handles.axes1);
imshow(image)
InfoImage = imfinfo(file_name);
Width = InfoImage.Width;
Height = InfoImage.Height;
Colortype = InfoImage.ColorType;
set(handles.edit1,'string',file_name);
set(handles.edit2,'string',path_name);
set(handles.edit8,'string',num2str(Width));
set(handles.edit9,'string',num2str(Height));
set(handles.edit10,'string',num2str(Colortype));

if (num2str(Colortype)=='grayscale')
        msgbox(sprintf('Please select RGB image'),'error','error');
end

    
% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
global image  

a=str2double(get(handles.edit4,'string'));

img=image

axes(handles.axes1);
imshow(image)
h = imrect;
position = getPosition(h);
croppedImage = imcrop(image, position);

col1 = position(1);%xmin
col2 = col1 + position(3); %xmin+ width
row1 = position(2);%ymin
row2 = row1 + position(4); %ymin+height

block1 = row2-row1+1 %height
block2 = col2-col1+1%width

set(handles.edit11,'string',num2str(block2)); 
set(handles.edit12,'string',num2str(block1));

if ((block1/a)<=10)&&((block2/a)<=10)
    
      b=a;
      processedImage=PixelatingEffect(croppedImage,b);
      img(row1:row2, col1:col2,:) = processedImage(1:(row2-row1+1), 1:(col2-col1+1),:);  
      axes(handles.axes1);
      imshow(img);
      
      set(handles.edit13,'string',num2str(ceil(block2/a))); 
      set(handles.edit14,'string',num2str(ceil(block1/a)));
    
      
else
    msgbox(sprintf('Please enter acceptable value '),'error','error');
end



     

% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)

global image 
img=image
c=str2double(get(handles.edit6,'string'));
axes(handles.axes1);

h = imrect;
position = getPosition(h);
croppedImage = imcrop(image, position);
processedImage=Blurring_effect(croppedImage,c);

col1 = position(1);
col2 = col1 + position(3);
row1 = position(2);
row2 = row1 + position(4);

block1 = row2-row1+1
block2 = col2-col1+1
set(handles.edit11,'string',num2str(block2)); 
set(handles.edit12,'string',num2str(block1))

img(row1:row2, col1:col2,:) = processedImage(1:(row2-row1+1), 1:(col2-col1+1),:);  
axes(handles.axes1)
imshow(img);



